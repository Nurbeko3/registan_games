/** BATTLE LEARN ARENA question bank — trilingual (uz/ru/en) + grade-tagged.
 *
 *  Every entry is a `LocalizedQuestion`: prompt/options/explain carry all three
 *  locales, and `localize()` (see ../lib/arena/localize) flattens one locale into
 *  the runtime question the engine renders. Admin-imported questions (Excel →
 *  Supabase) share this exact shape, so the cloud pool merges in seamlessly.
 *
 *  Adding a question = one append. Keep the option order identical across the
 *  three locales (the correct answer is `answer`, an index). Tone stays warm:
 *  `explain` teaches on a wrong answer, it never scolds.
 *
 *  `grade` (1–11) is the Uzbek school class — an admin/classroom classification,
 *  orthogonal to `difficulty`, which still drives arena matchmaking. */

import type { Difficulty, Grade } from '@/lib/arena/types';
import type { LocalizedQuestion } from '@/lib/arena/localize';

/** Compact builder for an mcq hardware question. Each locale array is
 *  `[prompt, option0, option1, option2, option3, explain]`; option 0 is correct. */
function hw(
  id: string,
  grade: Grade,
  difficulty: Difficulty,
  emoji: string,
  uz: string[],
  ru: string[],
  en: string[],
): LocalizedQuestion {
  return {
    id,
    type: 'mcq',
    category: 'hardware',
    difficulty,
    grade,
    emoji,
    answer: 0,
    prompt: { uz: uz[0], ru: ru[0], en: en[0] },
    options: { uz: uz.slice(1, 5), ru: ru.slice(1, 5), en: en.slice(1, 5) },
    explain: { uz: uz[5], ru: ru[5], en: en[5] },
  };
}

